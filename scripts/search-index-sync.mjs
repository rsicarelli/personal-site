#!/usr/bin/env node
/**
 * Search index sync (Option B — D1 FTS5) — turns the build-time corpus dump
 * (`dist/search-corpus.json`, emitted by `src/pages/search-corpus.json.ts`) into idempotent SQL
 * that (re)builds the `search_fts` virtual table, then optionally applies it to a D1 database via
 * `wrangler d1 execute`.
 *
 * Why a generated SQL file instead of `wrangler d1 migrations apply`: the repo deliberately ships
 * NO wrangler config (a config file makes Pages builds ignore dashboard env vars — bug #195), and
 * D1 has no multi-statement transactions, so we lean on idempotency for safety: the table is
 * `CREATE … IF NOT EXISTS`, then `DELETE FROM search_fts;` clears it, then every row is re-INSERTed.
 * Re-running fully replaces the index; a half-applied run is fixed by simply running again.
 *
 * Tokenizer: `unicode61 remove_diacritics 2` — diacritic folding (so `funcao` matches `função`)
 * plus Unicode word boundaries. NO porter stemmer on purpose: porter is English-only and would
 * mangle Portuguese; prefix matching (`"term"*` in the query) is the right bilingual tradeoff.
 *
 * Usage:
 *   node scripts/search-index-sync.mjs                 # write dist/search-index.sql only
 *   node scripts/search-index-sync.mjs --apply --db <name>   # write SQL, then apply to <name>
 *
 * The generated SQL file is gitignored (it's a build artifact derived from content).
 */
import { readFile, writeFile } from 'node:fs/promises';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const ROOT = fileURLToPath(new URL('..', import.meta.url));
const CORPUS = `${ROOT}dist/search-corpus.json`;
const OUT_SQL = `${ROOT}dist/search-index.sql`;

/** ~8000 chars of body per row — enough context for FTS relevance without bloating the index. */
const BODY_MAX = 8000;
/**
 * Rows per multi-row INSERT. D1 caps a single SQL statement (~100KB); with bodies up to BODY_MAX
 * chars a batch of 100 blew past it (SQLITE_TOOBIG), so we keep batches small — a handful of rows
 * per INSERT stays comfortably under the limit while still cutting round-trips vs one-row-per-stmt.
 */
const BATCH = 5;

/**
 * The FTS5 schema. `title`/`excerpt`/`body`/`tags` are the indexed (searchable) columns; everything
 * the renderer needs (`id`, `locale`, `type`, `url`, `type_label`, `meta_json`) rides along
 * UNINDEXED so a single MATCH query returns whole result rows with no JOIN. `IF NOT EXISTS` makes
 * the create idempotent; the column list/order is the contract the Pages Function reads back.
 */
const SCHEMA = `CREATE VIRTUAL TABLE IF NOT EXISTS search_fts USING fts5(
  title, excerpt, body, tags,
  id UNINDEXED, locale UNINDEXED, type UNINDEXED, url UNINDEXED,
  type_label UNINDEXED, meta_json UNINDEXED,
  tokenize="unicode61 remove_diacritics 2"
);`;

/**
 * Zero-result content-gap log (owner-private — no public read endpoint). The Pages Function upserts
 * into this on every zero-result query; we create it here so the table exists the moment the index
 * is first synced (the function also creates it defensively, but the sync is the source of truth).
 */
const ZERO_TERMS = `CREATE TABLE IF NOT EXISTS search_zero_terms (
  term TEXT, locale TEXT, count INTEGER NOT NULL DEFAULT 1,
  PRIMARY KEY (term, locale)
);`;

/** SQL single-quoted string literal: escape embedded quotes by doubling them (no bind params). */
function sqlStr(value) {
  return `'${String(value ?? '').replaceAll("'", "''")}'`;
}

/** Collapse a doc's tag list into one indexable, space-separated string. */
function tagsText(tags) {
  return Array.isArray(tags) ? tags.join(' ') : '';
}

/** Build the ordered column-value tuple for one corpus doc (matches the SCHEMA column order). */
function rowValues(doc) {
  const body = String(doc.body ?? '').slice(0, BODY_MAX);
  // meta is stored as JSON so the function can rebuild the localized meta line verbatim.
  const metaJson = JSON.stringify(doc.meta ?? []);
  return [
    sqlStr(doc.title),
    sqlStr(doc.excerpt),
    sqlStr(body),
    sqlStr(tagsText(doc.tags)),
    sqlStr(doc.id),
    sqlStr(doc.locale),
    sqlStr(doc.type),
    sqlStr(doc.url),
    sqlStr(doc.typeLabel),
    sqlStr(metaJson),
  ].join(', ');
}

/** Emit the full idempotent SQL program: schema, clear, then batched multi-row INSERTs. */
function buildSql(docs) {
  const cols = '(title, excerpt, body, tags, id, locale, type, url, type_label, meta_json)';
  const lines = [
    '-- Generated by scripts/search-index-sync.mjs — DO NOT EDIT (build artifact).',
    `-- ${docs.length} docs across all locales. Re-run to fully rebuild the index.`,
    SCHEMA,
    ZERO_TERMS,
    'DELETE FROM search_fts;',
  ];
  for (let i = 0; i < docs.length; i += BATCH) {
    const batch = docs.slice(i, i + BATCH);
    const tuples = batch.map((doc) => `(${rowValues(doc)})`).join(',\n  ');
    lines.push(`INSERT INTO search_fts ${cols} VALUES\n  ${tuples};`);
  }
  return lines.join('\n') + '\n';
}

function parseArgs(argv) {
  const args = { apply: false, db: '' };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === '--apply') args.apply = true;
    else if (argv[i] === '--db') args.db = argv[++i] ?? '';
  }
  return args;
}

async function main() {
  const { apply, db } = parseArgs(process.argv.slice(2));

  let raw;
  try {
    raw = await readFile(CORPUS, 'utf8');
  } catch {
    console.error(
      `[search-index-sync] ${CORPUS} not found. Run the Astro build first (it emits the corpus endpoint).`,
    );
    process.exit(1);
  }

  const { docs } = JSON.parse(raw);
  if (!Array.isArray(docs) || docs.length === 0) {
    console.error('[search-index-sync] corpus has no docs — refusing to wipe the index.');
    process.exit(1);
  }

  const sql = buildSql(docs);
  await writeFile(OUT_SQL, sql, 'utf8');
  console.log(`[search-index-sync] wrote ${OUT_SQL} (${docs.length} docs).`);

  if (!apply) return;
  if (!db) {
    console.error('[search-index-sync] --apply needs --db <database-name>.');
    process.exit(1);
  }

  // Apply remotely via wrangler — never `migrations apply` (no wrangler config in the repo, #195).
  console.log(`[search-index-sync] applying to D1 "${db}" (remote)…`);
  const res = spawnSync(
    'pnpm',
    ['wrangler', 'd1', 'execute', db, '--remote', `--file=${OUT_SQL}`, '--yes'],
    { stdio: 'inherit', cwd: ROOT },
  );
  if (res.status !== 0) {
    console.error(`[search-index-sync] wrangler exited with code ${res.status}.`);
    process.exit(res.status ?? 1);
  }
  console.log(`[search-index-sync] done — "${db}" index synced.`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
